package com.right.entity;

public class Module {
    private int module_id;

    private String module_name;

    private int module_order;

    private String module_url;

    private int up_module_id;

    private int module_control;

	public int getModule_id() {
		return module_id;
	}

	public void setModule_id(int module_id) {
		this.module_id = module_id;
	}

	public String getModule_name() {
		return module_name;
	}

	public void setModule_name(String module_name) {
		this.module_name = module_name;
	}

	public int getModule_order() {
		return module_order;
	}

	public void setModule_order(int module_order) {
		this.module_order = module_order;
	}

	public String getModule_url() {
		return module_url;
	}

	public void setModule_url(String module_url) {
		this.module_url = module_url;
	}

	public int getUp_module_id() {
		return up_module_id;
	}

	public void setUp_module_id(int up_module_id) {
		this.up_module_id = up_module_id;
	}

	public int getModule_control() {
		return module_control;
	}

	public void setModule_control(int module_control) {
		this.module_control = module_control;
	}

	

}